            <h2>L'ère des smartphones</h2>
            <p>Avec le développement de la photographie numérique, les appareils photos sont devenus 
              de plus en plus petits et capables de stocker de plus en plus d'images. 
              Cela se concrétise finalement en 2000 avec la commercialisation au Japon du tout premier 
              téléphone portable équipé d'un appareil photo (de 110 000 pixels et de 256 couleurs), 
              le Sharp J-SH04. Passant rapidement du simple gadget à l'accessoire indispensable que se doit de 
              posséder chaque téléphone portable moderne qui se respecte, 
              le nombre de pixels et de couleurs affichées par ses appareils se multiplient rapidement pour 
              des rendus de plus en plus proches de se que permettent les appareils photos traditionnels.
              </br>
              </br>
              La dernière grande évolution notable qui fit rentré les téléphones portables et 
              leurs appareils photos dans l'ère contemporaine qui nous connaissons aujourd'hui est l'invention 
              du premier smartphone moderne en 2007, le premier iPhone d'Apple. Depuis lors, 
              la qualité des appareils photos des smartphones a égalé celles des appareils professionnels 
              (atteignant, pour les appareils les plus récent, plus de 30 mégapixels), 
              il est maintenant possible de partager instantanément ses clichés à travers le monde avec Internet, 
              de prendre des photos panoramiques ou en 3D avec deux objectifs superposant deux images prises 
              sous un angle différent etc.
            </p>
