            <h2>L'ère numérique</h2>
            <p>Les décennies qui suivirent l'invention de la photographie furent riches en nouveautés : 
              le premier appareil photo polaroids, le premier appareil à flash photographique, 
              le premier appareil photo portable etc. mais l'une de ces plus grandes révolutions est sans conteste 
              l'invention de l'appareil photo numérique. Mais qu'est-ce cela signifie concrètement ?
              </br>
              </br>
              Un appareil photographique numérique est un appareil photo qui n'utilise pas de films ou de supports physiques 
              pour capter une image, mais qui la sauvegarde sur un support numérique, au départ sur des cassettes 
              puis des cartes mémoires et enfin, comme aujourd'hui, sur des disques durs internes. Cela permet de prendre et 
              garder plusieurs photos à la suite afin de les visionner plus tard sur ordinateur ou de les faire développer.
              </br>
              </br>
              On attribue généralement l'invention du premier appareil photo numérique à la société japonaise Sony en 1981 
              et qui pouvait enregistrer jusqu'à 50 photos sur une mini-disquette. L'idée fut ensuite largement reprise par 
              Kodak qui commercialisa les premiers appareil photo numériques à destination du grand public. 
              Le numérique étant à l'époque une technologie révolutionnaire qui se développait parallèlement 
              aux premiers ordinateurs, les évolutions dans le domaine se sont multipliés, comme l'apparition des premiers 
              reflex avec Canon. 
              On note aussi une très rapide évolution de la qualité des clichés.
            </p>
          </div>
        </div>

