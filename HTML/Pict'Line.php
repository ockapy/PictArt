            <h2>Les origines de la photographie</h2>
            <p>L'histoire des appareils photographiques et de la photographie est millénaire, 
              c'est le principe de la "chambre noire" qui sera repris et développé des siècles plus tard pour concevoir les premiers appareils photographiques.
            </br>
            </br>
            Ce concept a refait surface au X ème siècle : en effet, 
            ce principe était utilisé afin d'observer des éclipses dans des pièces complètement noires 
            dans lesquelles s'ouvrait un orifice qui permettait la projection de la lumière du soleil sur le mur 
            qui lui faisait face.
            </br>
            </br>
            Le premier à mettre au point un dispositif permettant de représenter une image du monde 
            sur un support papier fut Leonard de Vinci et sa "camera obscura", 
            qui fut utilisée à partir du XVI ème siècle, qui n'est autre le principe que nous venons de voir 
            mais appliqué à une boîte en bois : une lentille laissait passer la lumière dans la boite entièrement sombre dans laquelle se trouvait un miroir qui reflétait l'image. 
            L'artiste n'avait alors plus qu'à recopier sur papier le dessin reflété. On ne parle pas encore ici d'appareil photographique 
            car elle ne capturait pas une image du monde mais nécessitait encore l'intervention d'une personne pour recopier le dessin.
            </p>
          </div>
