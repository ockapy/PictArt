            <h2>L'ère moderne</h2>
            <p>La camera obscura fut utilisée jusqu'au XIX ème siècle où un ingénieur du nom de Nicéphore Niépce, 
              considéré comme l'inventeur de la photographie, 
              parvint à réalisé la première photographie de l'histoire en 1826 intitulée Point de vue du Gras, 
              non pas sur papier mais en captant les rayons du soleil sur une plaque en étain 
              et avec un temps de pose estimé à une dizaine d'heures.
              </br>
              </br>
              S'en suivra des appareils de plus en plus perfectionnés, 
              citons quelques grands noms comme Louis Daguerre qui reprendra l'appareil de Niépce 
              en réduisant le temps de pose à quelques minutes et qui en déposa le brevet le 7 janvier 1839 
              (date considérée comme l'invention officielle de la photographie), ou encore Thomas Sutton 
              qui prit en 1861 la première photographie couleur.
              </br>
              </br>
              Les premiers modèles de caméras un peu plus moderne consistaient en deux grandes boîtes 
              en bois qui étaient insérées l'une dans l'autre pour focaliser. À une extrémité se trouvait l'objectif, 
              tandis qu'à l'autre extrémité se trouvait un verre qui servait d'écran de mise au point et qui a ensuite 
              été remplacé par la plaque photosensible. Il est à noter que ces premiers appareils photos étaient autrefois 
              assez volumineux. De plus, ils étaient toujours utilisés sur des supports à trépieds, 
              car ils nécessitaient un certains temps de pose et devaient restés parfaitement immobiles durant cette durée. 
              C'est également au XIX siècle que la photographie devient un métier et un commerce : 
              de plus en plus de familles et personnalités aisées faisaient appel aux services de photographes.
            </p>
          </div>
        </div>

